using System;
using System.Collections.Generic;
using System.Text;
using urakawa.core;

namespace urakawa.events.core
{
    /// <summary>
    /// Arguments of the <see cref="TreeNode.ChildRemoved"/> event
    /// </summary>
	public class ChildRemovedEventArgs : TreeNodeEventArgs
	{
        /// <summary>
        /// Constructor setting the source <see cref="TreeNode"/> of the event,
        /// the <see cref="TreeNode"/> that was removed as a child and the index of the child prior to the removal
        /// </summary>
        /// <param name="notfr">The source <see cref="TreeNode"/> of the event</param>
        /// <param name="child"></param>
        /// <param name="pos"></param>
		public ChildRemovedEventArgs(TreeNode notfr, TreeNode child, int pos) : base(notfr)
		{
			RemovedChild = child;
			RemovedPosition = pos;
		}
        /// <summary>
        /// The <see cref="TreeNode"/> that was removed as a child
		/// </summary>
		public readonly TreeNode RemovedChild;
		/// <summary>
        /// The index of the child prior to the removal
		/// </summary>
		public readonly int RemovedPosition;
	}
}
