using System;
using System.Collections.Generic;
using System.Text;
using urakawa.core;

namespace urakawa.events.core
{
    /// <summary>
    /// Arguments of the <see cref="TreeNode.ChildAdded"/> event
    /// </summary>
    public class ChildAddedEventArgs : TreeNodeEventArgs
    {
        /// <summary>
        /// The child <see cref="TreeNode"/> that was added
        /// </summary>
        public readonly TreeNode AddedChild;

        /// <summary>
        /// Constructor setting the source <see cref="TreeNode"/> of the event
        /// and the child <see cref="TreeNode"/> that was added
        /// </summary>
        /// <param name="notfr">The source <see cref="TreeNode"/> of the event</param>
        /// <param name="child">The child <see cref="TreeNode"/> that was added</param>
        public ChildAddedEventArgs(TreeNode notfr, TreeNode child) : base(notfr)
        {
            AddedChild = child;
        }
    }
}